const generateTrend = (): number[] => {
  const base = Math.random() * 50000 + 1000;
  return Array.from({ length: 12 }, (_, i) => {
    const seasonal = Math.sin((i / 12) * Math.PI * 2) * 0.2;
    const trend = (i / 12) * 0.3;
    return Math.floor(base * (1 + seasonal + trend * (Math.random() - 0.5)));
  });
};

