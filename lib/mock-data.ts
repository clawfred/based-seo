export const getSerpResults = (keyword: string): SerpResult[] => {
  const domains = [
    "moz.com",
    "ahrefs.com",
    "semrush.com",
    "searchenginejournal.com",
    "neilpatel.com",
    "backlinko.com",
    "hubspot.com",
    "wordstream.com",
    "searchengineland.com",
    "contentmarketinginstitute.com",
  ];

  return Array.from({ length: 10 }, (_, i) => ({
    position: i + 1,
    url: `https://${domains[i]}/blog/${keyword.replace(/\s+/g, "-")}`,
    domain: domains[i],
    title: `${keyword.charAt(0).toUpperCase() + keyword.slice(1)} - Complete Guide ${i + 1}`,
  }));
};

